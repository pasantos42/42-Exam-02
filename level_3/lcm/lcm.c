int lcm (unsigned int a, unsigned int b)
{
	if (a != 0 && b != 0)
	{
		int i = 2;
		while (i <= a && i <= b)
		{
			if (a % i == 0 && b % i == 0)
				return (i);
			i++;
		}
	}
	return (0);
}
#include <unistd.h>

int main (int argc, char**argv)
{
    if (argc == 2)
    {
        int c, i = 0;
        while ((c = argv[1][i]))
        {
            if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
            {
                if (c == 'z' || c == 'Z')
                    c -= 26;
                c++;
            }
            write (1, &c, 1);
            i++;
        }
    }
    write(1, "\n", 1);
    return(0);
}